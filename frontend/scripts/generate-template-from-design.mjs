#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

function readText(filePath) {
	return fs.readFileSync(filePath, 'utf8');
}

function writeText(filePath, content) {
	fs.mkdirSync(path.dirname(filePath), { recursive: true });
	fs.writeFileSync(filePath, content, 'utf8');
}

function parseScalar(raw) {
	const value = raw.trim();
	if (!value.length) return '';
	if (
		(value.startsWith('"') && value.endsWith('"')) ||
		(value.startsWith("'") && value.endsWith("'"))
	) {
		return value.slice(1, -1);
	}
	if (/^-?\d+(\.\d+)?$/.test(value)) return Number(value);
	if (value === 'true') return true;
	if (value === 'false') return false;
	if (value === 'null') return null;
	return value;
}

function parseSimpleYaml(yamlText) {
	const root = {};
	const lines = yamlText.replace(/\r\n/g, '\n').split('\n');
	const stack = [{ indent: -1, obj: root }];

	for (const rawLine of lines) {
		if (!rawLine.trim() || rawLine.trim().startsWith('#')) continue;
		const indent = rawLine.match(/^ */)[0].length;
		const line = rawLine.trim();
		const sepIndex = line.indexOf(':');
		if (sepIndex <= 0) continue;
		const key = line.slice(0, sepIndex).trim();
		const rest = line.slice(sepIndex + 1).trim();

		while (stack.length > 1 && indent <= stack[stack.length - 1].indent) {
			stack.pop();
		}
		const parent = stack[stack.length - 1].obj;

		if (!rest.length) {
			parent[key] = {};
			stack.push({ indent, obj: parent[key] });
			continue;
		}

		parent[key] = parseScalar(rest);
	}
	return root;
}

function parseDesignFrontmatter(markdownText) {
	const match = markdownText.match(/^---\r?\n([\s\S]*?)\r?\n---/);
	if (!match) {
		throw new Error('DESIGN.md must start with YAML frontmatter wrapped by ---');
	}
	return parseSimpleYaml(match[1]);
}

function asCssLength(value, fallback) {
	if (value === undefined || value === null || value === '') return fallback;
	return typeof value === 'number' ? `${value}px` : String(value);
}

function resolveDesignRef(value, design) {
	if (typeof value !== 'string') return value;
	const match = value.trim().match(/^\{([a-zA-Z0-9_-]+)\.([a-zA-Z0-9_.-]+)\}$/);
	if (!match) return value;
	const [, scope, keyPath] = match;
	let cursor = design[scope];
	if (!cursor || typeof cursor !== 'object') return value;
	for (const key of keyPath.split('.')) {
		if (!cursor || typeof cursor !== 'object' || !(key in cursor)) return value;
		cursor = cursor[key];
	}
	return cursor;
}

function buildGeneratedScss({ packName, design }) {
	const colors = design.colors || {};
	const typography = design.typography || {};
	const spacing = design.spacing || {};
	const components = design.components || {};

	const canvas = colors.canvas || '#080808';
	const surface = colors.surface || '#141414';
	const text = colors['on-canvas'] || '#f5f5f3';
	const textSecondary = colors['on-canvas-secondary'] || '#6d6d6d';
	const border = colors.border || surface;
	const primary = colors.primary || text;
	const onPrimary = colors['on-primary'] || canvas;

	const labelTypo = typography.label || {};
	const labelSmTypo = typography['label-sm'] || labelTypo;
	const inputTypo = typography.input || {};
	const bodyTypo = typography.body || {};

	const ghost = components['button-ghost'] || {};
	const primaryBtn = components['button-primary'] || {};
	const input = components.input || {};

	const pageX = asCssLength(spacing['page-x'], '32px');
	const gridGap = asCssLength(spacing.gap, '2px');

	return `// AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.
// Generated by: pnpm run design:sync ${packName.toLowerCase()}

.tpl-pack-${packName.toLowerCase()} {
	--ds-canvas: ${canvas};
	--ds-surface: ${surface};
	--ds-text: ${text};
	--ds-text-secondary: ${textSecondary};
	--ds-border: ${border};
	--ds-primary: ${primary};
	--ds-on-primary: ${onPrimary};

	--ds-page-x: ${pageX};
	--ds-grid-gap: ${gridGap};

	--ds-font-ui: ${labelTypo.fontFamily || 'DM Mono'};
	--ds-font-body: ${bodyTypo.fontFamily || 'DM Sans'};
	--ds-font-input: ${inputTypo.fontFamily || labelTypo.fontFamily || 'DM Mono'};
}

.tpl-pack-${packName.toLowerCase()} .tp-btn-ghost {
	background: ${resolveDesignRef(ghost.backgroundColor, design) || 'transparent'};
	color: ${resolveDesignRef(ghost.textColor, design) || 'var(--ds-text-secondary)'};
	border: 1px solid var(--ds-border);
	border-radius: ${asCssLength(resolveDesignRef(ghost.rounded, design), '0')};
	padding: ${ghost.padding || '4px 10px'};
	font-family: var(--ds-font-ui);
	font-size: ${asCssLength(labelSmTypo.fontSize, '9px')};
	letter-spacing: ${labelSmTypo.letterSpacing || '0.2em'};
	text-transform: uppercase;
}

.tpl-pack-${packName.toLowerCase()} .tp-btn-primary {
	background: ${resolveDesignRef(primaryBtn.backgroundColor, design) || 'var(--ds-primary)'};
	color: ${resolveDesignRef(primaryBtn.textColor, design) || 'var(--ds-on-primary)'};
	border: 1px solid var(--ds-border);
	border-radius: ${asCssLength(resolveDesignRef(primaryBtn.rounded, design), '0')};
	padding: ${primaryBtn.padding || '12px 28px'};
	font-family: var(--ds-font-ui);
	font-size: ${asCssLength(labelSmTypo.fontSize, '9px')};
	letter-spacing: ${labelSmTypo.letterSpacing || '0.2em'};
	text-transform: uppercase;
}

.tpl-pack-${packName.toLowerCase()} .tp-input {
	background: ${resolveDesignRef(input.backgroundColor, design) || 'var(--ds-surface)'};
	color: ${resolveDesignRef(input.textColor, design) || 'var(--ds-text)'};
	border: 1px solid var(--ds-border);
	border-radius: ${asCssLength(resolveDesignRef(input.rounded, design), '0')};
	padding: ${input.padding || '10px 14px'};
	font-family: var(--ds-font-input);
	font-size: ${asCssLength(inputTypo.fontSize, '13px')};
	letter-spacing: ${inputTypo.letterSpacing || '0.04em'};
}
`;
}

function mergeThemeDefaults(current, design) {
	const colors = design.colors || {};
	const typography = design.typography || {};
	const spacing = design.spacing || {};

	const merged = { ...current };
	merged.displayName = design.name || current.displayName;

	merged.packTokens = {
		...(current.packTokens || {}),
		layout: {
			...((current.packTokens || {}).layout || {}),
			padX: asCssLength(spacing['page-x'], ((current.packTokens || {}).layout || {}).padX || '32px'),
			gapGrid: asCssLength(spacing.gap, ((current.packTokens || {}).layout || {}).gapGrid || '2px')
		}
	};

	merged.colors = {
		...(current.colors || {}),
		background: colors.canvas || (current.colors || {}).background,
		surfaceCard: colors.surface || (current.colors || {}).surfaceCard,
		surfaceCardSecondary: colors['surface-2'] || (current.colors || {}).surfaceCardSecondary,
		text: colors['on-canvas'] || (current.colors || {}).text,
		secondary: colors['on-canvas-secondary'] || (current.colors || {}).secondary,
		muted: colors['on-canvas-secondary'] || (current.colors || {}).muted,
		textSubtle: colors['on-canvas-muted'] || (current.colors || {}).textSubtle,
		borderSubtle: colors.border || (current.colors || {}).borderSubtle,
		primary: colors.primary || (current.colors || {}).primary,
		accent: colors.primary || (current.colors || {}).accent
	};

	merged.fonts = {
		...(current.fonts || {}),
		heading: (typography.heading || {}).fontFamily || (current.fonts || {}).heading,
		body: (typography.body || {}).fontFamily || (current.fonts || {}).body,
		links: (typography.label || {}).fontFamily || (current.fonts || {}).links,
		lists: (typography.label || {}).fontFamily || (current.fonts || {}).lists,
		formInputs: (typography.input || {}).fontFamily || (current.fonts || {}).formInputs,
		formLabels: (typography.label || {}).fontFamily || (current.fonts || {}).formLabels
	};

	return merged;
}

function ensureStylesEntrypointImport(stylesEntrypointPath) {
	let content = readText(stylesEntrypointPath);
	if (content.includes("@use './design.generated';")) return;
	content = `${content.trimEnd()}\n@use './design.generated';\n`;
	writeText(stylesEntrypointPath, content);
}

function run() {
	const rootDir = process.cwd();
	const pack = (process.argv[2] || 'noir').trim().toLowerCase();
	const packDir = path.join(rootDir, 'src', 'templates', pack);
	const designPath = path.join(packDir, 'DESIGN.md');
	const themePath = path.join(packDir, 'theme.defaults.json');
	const stylesEntrypointPath = path.join(packDir, 'styles', 'styles.scss');
	const stylesGeneratedPath = path.join(packDir, 'styles', '_design.generated.scss');

	if (!fs.existsSync(designPath)) {
		throw new Error(`DESIGN.md not found at: ${designPath}`);
	}
	if (!fs.existsSync(themePath)) {
		throw new Error(`theme.defaults.json not found at: ${themePath}`);
	}
	if (!fs.existsSync(stylesEntrypointPath)) {
		throw new Error(`styles entrypoint not found at: ${stylesEntrypointPath}`);
	}

	const designMd = readText(designPath);
	const design = parseDesignFrontmatter(designMd);
	const currentTheme = JSON.parse(readText(themePath));
	const nextTheme = mergeThemeDefaults(currentTheme, design);
	const scss = buildGeneratedScss({ packName: pack, design });

	writeText(themePath, `${JSON.stringify(nextTheme, null, '\t')}\n`);
	writeText(stylesGeneratedPath, scss);
	ensureStylesEntrypointImport(stylesEntrypointPath);

	console.log(`Synced template pack "${pack}" from DESIGN.md`);
	console.log(`- Updated: ${themePath}`);
	console.log(`- Updated: ${stylesGeneratedPath}`);
	console.log(`- Ensured import in: ${stylesEntrypointPath}`);
}

try {
	run();
} catch (error) {
	console.error(error instanceof Error ? error.message : String(error));
	process.exit(1);
}
